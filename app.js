var express = require('express');
var path = require('path');
var favicon = require('serve-favicon');
var logger = require('morgan');
var cookieParser = require('cookie-parser');
var bodyParser = require('body-parser');

var routes = require('./routes/');
var promotions = require('./routes/promotions');
var payments = require('./routes/payments');
var flavours = require('./routes/flavours');

var clientend = require('./routes/clientend');
var companyend = require('./routes/companyend');
var community = require('./routes/community');
var createDB = require('./routes/createDB');

var subscription = require('./routes/subscription');
var identify = require('./routes/identify');

var test = require('./routes/test');

var app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'jade');

// uncomment after placing your favicon in /public
//app.use(favicon(__dirname + '/public/favicon.ico'));
app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/', routes);

app.use('/promotions', promotions);
app.use('/payments', payments);
app.use('/flavours', flavours);

app.use('/clientend',clientend);
app.use('/companyend',companyend);

app.use('/community',community);
app.use('/createDB',createDB);
app.use('/subscription',subscription);
app.use('/identify',identify);

app.use('/test',test);

// catch 404 and forward to error handler
app.use(function(req, res, next) {
  var err = new Error('Not Found');
  err.status = 404;
  next(err);
});

// error handlers

// development error handler
// will print stacktrace
if (app.get('env') === 'development') {
  app.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.render('error', {
      message: err.message,
      error: err
    });
  });
}

// production error handler
// no stacktraces leaked to user
app.use(function(err, req, res, next) {
  res.status(err.status || 500);
  res.render('error', {
    message: err.message,
    error: {}
  });
});


module.exports = app;
